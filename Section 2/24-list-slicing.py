friends = ["Rolf", "Charlie", "Anna", "Bob", "Jen"]

# Output: ["Anna", "Bob"]
print(friends[2:4])

# Output: ['Charlie', 'Anna', 'Bob', 'Jen'] - From 1
print(friends[1:])

# Output: ['Rolf', 'Charlie', 'Anna', 'Bob'] - Up to 4
print(friends[:4])

# Output ["Anna", "Bob", "Jen"] - 3 from end to the end
print(friends[-3:])

# Output ["Rolf", "Charlie", "Anna"] - start to second to last element
print(friends[-1:])